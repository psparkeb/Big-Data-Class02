#벡터: 데이터 분석에서 가장 중요한 자료. 스칼라 값을 일렬로 나열한 구조. 달걀 5개를 줄줄이~
#      위에서 아래로 순서가 있음. 문자면 문자, 숫자는 숫자 같은 자료형을 가지고 있음.
1:10
c(T,T,F)
#벡터를 만드는 가장 많이 사용하는 함수. 컴바인 c
