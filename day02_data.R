nums <-c(a,b)
#데이터 조회는 [ ] 대괄호로 방을 지정함. 객체[1]: 객체의 1번째 방을 조회
nums[3] #하나 꺼내면 스칼라, 두 개 이상 꺼내면 벡터(일렬로 나열되므로)
nums[3:4] #R이나 판다스에서 이렇게 사용
nums[c(3,4)]
nums[c(3,4)][1]
